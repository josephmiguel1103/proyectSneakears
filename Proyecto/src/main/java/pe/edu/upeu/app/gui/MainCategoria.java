    //Listar en la tabla
    public void ListarCategoria() {
        cDao = new CategoriaDAO();
        List<CategoriaTO> listarCategoria = cDao.listarCategoria();
        jTable1.setAutoCreateRowSorter(true);
        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        Object[] ob = new Object[3];
        for (int i = 0; i < listarCategoria.size(); i++) {
            ob[0] = i+1;
            ob[1] = listarCategoria.get(i).getIdCategoria();
            ob[2] = listarCategoria.get(i).getNombre();
            modelo.addRow(ob);
        }
        jTable1.setModel(modelo);
    }
    
    //Pintar los txt
    private void paintForm() {
        if (jTable1.getSelectedRow() != -1) {
            modelo = (DefaultTableModel) jTable1.getModel();
            int rowx = jTable1.getSelectedRow();
            Object valor = jTable1.getValueAt(rowx, 1);
            cDao = new CategoriaDAO();
            CategoriaTO d = cDao.buscarCategoria(Integer.parseInt(valor.toString()));
            txtIdCategoria.setText(String.valueOf(d.getIdCategoria()));
            txtNombre.setText(d.getNombre());
            btnRegistrar.setText("MODIFICAR");
        } else {
            txtNombre.setEditable(true);
        }
    }
    
    //Vaciar los txt
    public void resetForm() {
        txtIdCategoria.setText("");
        txtNombre.setText("");
        txtNombre.requestFocus();

    }

